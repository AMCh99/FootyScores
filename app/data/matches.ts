export type MatchStage =
  | "Group Stage"
  | "Quarter-Final"
  | "Semi-Final"
  | "Bronze Medal Match"
  | "Gold Medal Match";

export type Gender = "Men" | "Women";

export interface Match {
  id: string;
  date: string;
  time: string;
  homeTeam: string;
  awayTeam: string;
  homeTeamCode: string;
  awayTeamCode: string;
  venue: string;
  stage: MatchStage;
  gender: Gender;
  group?: string;
}

export function generateEndpoint(match: Match): string {
  return `/api/v1/matches/${match.id}`;
}

export function generateMatchId(
  date: string,
  homeTeamCode: string,
  awayTeamCode: string
): string {
  return `${date}-${homeTeamCode.toLowerCase()}-${awayTeamCode.toLowerCase()}`;
}

