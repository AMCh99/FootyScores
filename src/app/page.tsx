"use client";

import { useCallback, useEffect, useMemo, useRef, useState } from "react";

import { MatchEndpointCard } from "@/components/MatchEndpointCard";
import { StatePanel } from "@/components/StatePanel";
import { errorMessages } from "@/lib/errors/messages";
import { exportEndpointsToJson } from "@/lib/export/exportJson";
import type { GeneratedMatchRecord, GenerationResult } from "@/lib/types/domain";
import type { MatchEndpoint } from "@/lib/types/endpoint";

interface ApiErrorResponse {
  error?: {
    title?: string;
    detail?: string;
  };
}

function getErrorMessage(payload: ApiErrorResponse | null): string {
  if (!payload?.error) {
    return errorMessages.generationFailedDetail;
  }

  const title = payload.error.title ?? errorMessages.generationFailedTitle;
  const detail = payload.error.detail ?? errorMessages.generationFailedDetail;

  return `${title}: ${detail}`;
}

function downloadText(fileName: string, content: string): void {
  const blob = new Blob([content], {
    type: "application/json",
  });
  const url = URL.createObjectURL(blob);
  const anchor = document.createElement("a");

  anchor.href = url;
  anchor.download = fileName;
  document.body.appendChild(anchor);
  anchor.click();
  anchor.remove();
  URL.revokeObjectURL(url);
}

function downloadAllEndpoints(fileName: string, endpoints: MatchEndpoint[]): void {
  downloadText(fileName, exportEndpointsToJson(endpoints));
}

function downloadSingleEndpoint(fileName: string, endpoint: MatchEndpoint): void {
  downloadText(fileName, JSON.stringify(endpoint, null, 2));
}

function formatDateTime(value: string): string {
  const parsed = new Date(value);

  if (Number.isNaN(parsed.getTime())) {
    return value;
  }

  return new Intl.DateTimeFormat("en-GB", {
    dateStyle: "medium",
    timeStyle: "short",
  }).format(parsed);
}

export default function HomePage() {
  const hasAutoGeneratedRef = useRef<boolean>(false);
  const [isLoading, setIsLoading] = useState<boolean>(false);
  const [error, setError] = useState<string>("");
  const [result, setResult] = useState<GenerationResult | null>(null);
  const [selectedRecord, setSelectedRecord] = useState<GeneratedMatchRecord | null>(null);

  const endpoints = useMemo(() => {
    if (!result) {
      return [];
    }

    return result.matches.map((item: GenerationResult["matches"][number]) => item.endpoint);
  }, [result]);

  const handleGenerate = useCallback(async (): Promise<void> => {
    setIsLoading(true);
    setError("");

    try {
      const response = await fetch("/api/generate", {
        method: "POST",
      });

      if (!response.ok) {
        const payload = (await response.json().catch(() => null)) as ApiErrorResponse | null;
        setResult(null);
        setError(getErrorMessage(payload));
        return;
      }

      const payload = (await response.json()) as GenerationResult;

      setResult(payload);
      setSelectedRecord(null);
    } catch {
      setResult(null);
      setSelectedRecord(null);
      setError(errorMessages.generationFailedDetail);
    } finally {
      setIsLoading(false);
    }
  }, []);

  useEffect(() => {
    if (hasAutoGeneratedRef.current) {
      return;
    }

    hasAutoGeneratedRef.current = true;
    void handleGenerate();
  }, [handleGenerate]);

  const handleExport = (): void => {
    if (endpoints.length === 0) {
      return;
    }

    downloadAllEndpoints("olympic-football-endpoints.json", endpoints);
  };

  const handleOpenApi = (): void => {
    window.open("/api/generate", "_blank", "noopener,noreferrer");
  };

  const handleOpenSingleMatchApi = (matchCode: string): void => {
    const encodedMatchCode = encodeURIComponent(matchCode);
    window.open(`/api/generate?matchCode=${encodedMatchCode}`, "_blank", "noopener,noreferrer");
  };

  const handleExportSingleMatch = (record: GeneratedMatchRecord): void => {
    downloadSingleEndpoint(`olympic-football-endpoint-${record.source.matchCode}.json`, record.endpoint);
  };

  return (
    <main className="match-workspace">
      <header className="workspace-header">
        <div>
          <p className="workspace-eyebrow">Paris 2024 Football Validation</p>
          <h1>Match Inspection Console</h1>
          <p className="workspace-subtitle">
            Each list item shows the football match and its generated endpoint preview. Select any item to inspect full details.
          </p>
        </div>

        <div className="workspace-actions">
          <button className="control-button secondary" onClick={handleOpenApi} type="button">
            Open API JSON
          </button>
          <button
            className="control-button secondary"
            onClick={handleExport}
            disabled={endpoints.length === 0}
            type="button"
          >
            Export All
          </button>
          <button className="control-button" onClick={handleGenerate} disabled={isLoading} type="button">
            {isLoading ? "Refreshing..." : "Reload Matches"}
          </button>
        </div>
      </header>

      {isLoading && (
        <section className="loading-stage" role="status" aria-live="polite">
          <div className="loader-orbit" aria-hidden="true">
            <span className="loader-ring" />
            <span className="loader-ring delay" />
          </div>
          <h2>Loading Match Data</h2>
          <p>Retrieving official Olympic football feeds and generating deterministic endpoint payloads.</p>

          <div className="loading-skeleton-grid" aria-hidden="true">
            <div className="loading-skeleton-card" />
            <div className="loading-skeleton-card" />
            <div className="loading-skeleton-card" />
          </div>
        </section>
      )}

      {!isLoading && error.length > 0 && (
        <StatePanel title={errorMessages.generationFailedTitle} message={error} isError />
      )}

      {!isLoading && !error && result && result.matches.length === 0 && (
        <StatePanel
          title="No Football Matches Found"
          message="No football matches were generated from the official source."
        />
      )}

      {!isLoading && !error && result && result.matches.length > 0 && (
        !selectedRecord ? (
          <section className="match-list-only">
            <aside className="match-list-panel" aria-label="Generated football match list">
              <header>
                <h2>Matches</h2>
                <p>{result.matches.length} fixtures available</p>
                <p className="list-meta">Generated at {formatDateTime(result.diagnostics.generatedAt)}</p>
              </header>

              <div className="match-list-scroll">
                {result.matches.map((record: GenerationResult["matches"][number]) => (
                  <MatchEndpointCard
                    key={record.source.matchCode}
                    record={record}
                    isSelected={false}
                    onOpen={setSelectedRecord}
                  />
                ))}
              </div>
            </aside>
          </section>
        ) : (
          <section className="master-detail-layout">
            <aside className="match-list-panel" aria-label="Generated football match list">
              <header>
                <h2>Matches</h2>
                <p>{result.matches.length} fixtures available</p>
                <p className="list-meta">Generated at {formatDateTime(result.diagnostics.generatedAt)}</p>
              </header>

              <div className="match-list-scroll">
                {result.matches.map((record: GenerationResult["matches"][number]) => (
                  <MatchEndpointCard
                    key={record.source.matchCode}
                    record={record}
                    isSelected={selectedRecord?.source.matchCode === record.source.matchCode}
                    onOpen={setSelectedRecord}
                  />
                ))}
              </div>
            </aside>

            <section className="match-detail-panel" aria-live="polite">
              <header className="detail-header">
                <div>
                  <p className="detail-status">{selectedRecord.endpoint.status}</p>
                  <h2>
                    {selectedRecord.endpoint.teams.home} vs {selectedRecord.endpoint.teams.away}
                  </h2>
                  <p>
                    {formatDateTime(selectedRecord.endpoint.kickoff)} | {selectedRecord.endpoint.venue.name},{" "}
                    {selectedRecord.endpoint.venue.city}
                  </p>
                </div>

                <div className="detail-score" aria-label="Match score">
                  <span>{selectedRecord.endpoint.score.home}</span>
                  <small>:</small>
                  <span>{selectedRecord.endpoint.score.away}</span>
                </div>
              </header>

              <div className="detail-actions">
                <button
                  className="control-button secondary"
                  onClick={() => handleOpenSingleMatchApi(selectedRecord.source.matchCode)}
                  type="button"
                >
                  Open Single API
                </button>
                <button
                  className="control-button secondary"
                  onClick={() => handleExportSingleMatch(selectedRecord)}
                  type="button"
                >
                  Export This Match
                </button>
              </div>

              <dl className="detail-meta-grid">
                <div>
                  <dt>Match Code</dt>
                  <dd>{selectedRecord.source.matchCode}</dd>
                </div>
                <div>
                  <dt>Event Code</dt>
                  <dd>{selectedRecord.source.eventCode}</dd>
                </div>
                <div>
                  <dt>Round</dt>
                  <dd>{selectedRecord.endpoint.competition.round}</dd>
                </div>
                <div>
                  <dt>Source Mode</dt>
                  <dd>{selectedRecord.source.sourceMode}</dd>
                </div>
              </dl>

              <div className="detail-json-grid">
                <article>
                  <h3>Source Match Data</h3>
                  <pre>{JSON.stringify(selectedRecord.source, null, 2)}</pre>
                </article>

                <article>
                  <h3>Generated Endpoint</h3>
                  <pre>{JSON.stringify(selectedRecord.endpoint, null, 2)}</pre>
                </article>
              </div>
            </section>
          </section>
        )
      )}
    </main>
  );
}
